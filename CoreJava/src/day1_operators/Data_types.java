			//6.double
			//size-8 bytes-64 bits
			//after . it can take upto 15 digits
			double d=22.333;
			//7.char
			//size -2 bytes---16 bits
			// a-z
			//A-Z
			//0=9
			//special single character
			
			char c1='a';
			char c2='A';
			char c3='2';
			char c4='*';
			//8.boolean
			//size--~ approximately 1 bit
