package Day_2;

public class MathematicalOperations {

	public static void main(String[] args) {
	
		System.out.println(9/3);//3
		System.out.println(9/2);//4
		System.out.println(9.0/2);//4.5
		System.out.println(9/2.0);//4.5
		System.out.println(9.0/2.0);//4.5
		
		System.out.println((float)9/2);//4.5
		
		System.out.println(0/9);//0
		//System.out.println(9/0);//ArithmeticException
		//System.out.println(0/0); any numer divided by zero will get ArithmeticException 
		
		System.out.println(9.0/0);//any floating and double number divide by zero 
		//infinity will get
		
		System.out.println(9/0.0);//Infinity
		System.out.println(9.0/0.0);//Infinity
		System.out.println(9/0.0);//Infinity
		
		System.out.println(0.0/2.0);//0.0
		System.out.println((float)9/0);//Infinity
        
		System.out.println(0.0/0);//NAN(Not a number)
		System.out.println(0/0.0);//NAN(Not a number)
		System.out.println(0.0/0.0);//NaN
        
		System.out.println('a'/2);//48
		System.out.println(9%3);// reminder is zero
		
		
		
		
	}

}
